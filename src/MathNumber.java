public class MathNumber {
	
	 public static void main(String[] args) {
	       
	     double result;
	     
	     result = Math.pow(2, 3);
	     result = Math.abs(2);
	     result = Math.sqrt(9);
	     result = Math.round(2.14);
	     result = Math.ceil(3.14);
	     result = Math.floor(3.99);
	     result = Math.max(2, 3);
	     result = Math.min(2, 3);
	     System.out.println(result);
	     System.out.println(Math.PI);
	     System.out.println(Math.E);
	     System.out.println(result);
	    }

}
